
mood = input("What mood are you in: ")

if mood == "happy":
    print("It's great to see you happy.")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up!")
elif mood == "excited":
    print("Woohooo!")
elif mood == "relaxed":
    print("Yeah, today is the day to chill...")
else:
    print("I don't recognize this mood.")
